    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        model = (DefaultTableModel) jTable1.getModel();
        if (model.getRowCount() > 0) {
            String[] value = new String[5];
            String email = UserDashboard.userEmail.getText();
            value = pdao.getUsersValue(email);
            int uid = Integer.parseInt(value[0]);
            String ufname = value[1];
            String ulname = value[2];
            String uphone = value[3];
            String uaddress = value[4];
            String purchaseDate = df.format(date);
            for (int i = 0; i < model.getRowCount(); i++) {
                int id = Integer.parseInt(model.getValueAt(i, 0).toString());
                int piid = Integer.parseInt(model.getValueAt(i, 1).toString());
                String pname = model.getValueAt(i, 2).toString();
                int q = Integer.parseInt(model.getValueAt(i, 3).toString());
                double pprice = Double.parseDouble(model.getValueAt(i, 4).toString());
                double tot = Double.parseDouble(model.getValueAt(i, 5).toString());
                pdao.insert(id, uid, ufname, ulname, uphone, piid, pname, q, pprice, tot, purchaseDate, uaddress);
                int newQuantity = pdao.getQty(piid) - q;
                pdao.updateQty(piid, newQuantity);
                JOptionPane.showMessageDialog(this, "Purchase Successful!");
                setDefault();
            }
        } else {
            JOptionPane.showMessageDialog(this, "You haven't purchased any product", "Warning", 2);

        }
    }//GEN-LAST:event_jButton2ActionPerformed

